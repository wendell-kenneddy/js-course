// Document Object Model

/*
  DOM is the browser API that allow us to manipulate the html document using
  Javascript. DOM is a tree-like data structure, so elements can have nodes
  (childs) and parents.

  const x = document.getElementById('demo');
  console.log(x);

  In the example above, we are selecting the element with the ID 'demo' in the
  html document, and showing it's content in the console.

  Elements in the DOM tree are treated like objects, so they have methods as
  well, such as innerHTML, innerText, style and so on.
*/

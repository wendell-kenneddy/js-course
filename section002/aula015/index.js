// The Math object

/*
  Javascript has some built-in function that can helps us do some operations.
  Below are some basic examples:

  1. Math.random() -> Returns a random number given the interval [min, max[
    console.log(Math.random()) // -> [0, 1[
    console.log(Math.random() * 10) // -> [0, 10[
    console.log(Math.random() * (max - min) + max) // Between arbitrary values
      Ex: console.log(Math.random() * (10 - 5) + 5) // ->[5, 10[

  2. Math.ceil() -> Rounds up the number
    console.log(Math.ceil(9.51)) // -> 10
    console.log(Math.ceil(9.49)) // -> 10

  3. Math.floor() -> Rounds down the number
    console.log(Math.floor(9.51)) // -> 9
    console.log(Math.floor(9.49)) // -> 9

  4. Math.round() -> Rounds up ou down the number
    If n < 5, rounds down, if n > 5, rounds up
    console.log(Math.round(9.51)) // -> 10
    console.log(Math.round(9.49)) // -> 9

  These are just some of the possible methods available. There are much more.
*/

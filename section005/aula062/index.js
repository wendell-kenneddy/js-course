// Arrays revision

/*
  Arrays are a data structure based of indexed items in a sort of list. We can
  use some methods in arrays, the basics are push, shift, unshift, slice, split
  and join.

  const myArray = ['Orange', 'Apple', 'Lemon'];
  myArray.push('Grape');
  console.log(myArray); // -> Orange, Apple, Lemon, Grape

  myArray.shift();
  console.log(myArray); // -> Apple, Lemon, Grape

  myArray.unshift('Orange');
  console.log(myArray); // -> Orange, Apple, Lemon, Grape

  console.log(myArray.slice(0, -2)) // -> Orange, Apple

  console.log(myArray.join('--')) // -> Orange--Apple--Lemon--Grape

  const myString = 'Alone in a room';
  const newArray = myString.split(' ');
  console.log(newArray) // -> Alone, in, a, room
*/

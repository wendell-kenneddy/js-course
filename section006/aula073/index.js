// Useful Object methods

/*
  Object.assign({...}) -> copies the key-value pairs of an object to another object,
  without making both reference the same memory address.

  Object.values({...}) -> returns an array containing the values of the keys in the
  object.

  Object.getOwnPropertyDescriptor({...}, 'prop') -> returns the descriptor of
  the desired property.

  Object.entries({...}) -> returns an array containing arrays containing two
  indexes, being the first the key name of the entry, and the second index the
  value of the key.

  Object.freeze({...}) -> prevents the object of being modifified, reconfigured,
  deleted and etc.
*/
